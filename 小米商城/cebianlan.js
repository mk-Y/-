var w = 0
$('.leftBar>a').hover(function(event){
	$(this).addClass('active')
	$('#content').html('')
	console.log(this)
	// 根据移动到a的index索引值,取出对应的数据
	var data = arr1[$(this).attr('index')].data
	console.log(data)
	for(var idx in data){
		$('<section>')
		.attr('class','item')
		.appendTo($('#content'))
		.append($('<img>').attr('src',data[idx].src))
		.append($('<span>').text(data[idx].name))
		
	}
	w = Math.ceil(data.length/6)*250
	$('#content').width(Math.ceil(data.length/6)*250)
},function(){
	$(this).removeClass('active')
	$('#content').width(0)
})
$('#content').hover(function(){
	$('#content').width(w)
},function(){
	$('#content').width(0)
})












